AS
CREATE TABLE Departments
(
Id INT IDENTITY (1,1)  NOT NULL, CONSTRAINT PK_Departments PRIMARY KEY CLUSTERED (Id),
NAME NVARCHAR(100) NOT NULL,
)
CREATE TABLE EmployeeTypes
(
Id INT IDENTITY (1,1)  NOT NULL, CONSTRAINT PK_EmployeeType PRIMARY KEY CLUSTERED (Id),
Description NVARCHAR(50) NOT NULL
)
CREATE TABLE Employees
   (
      Id INT UNIQUE IDENTITY (100000,1)  NOT NULL, CONSTRAINT PK_Employees PRIMARY KEY CLUSTERED (Id),

	  UserId NVARCHAR(450) NOT NULL UNIQUE,
	  CONSTRAINT FK_Employees_To_AspNetUsers FOREIGN KEY (UserId)
      REFERENCES AspNetUsers (Id)
      ON DELETE CASCADE
      ON UPDATE CASCADE,
	  
	  FirstName NVARCHAR(25) NOT NULL,
	  MiddleName NVARCHAR(25),
	  LastName NVARCHAR(25) NOT NULL,

	  DepartmentId INT NOT NULL,
	  CONSTRAINT FK_Employees_To_Departments FOREIGN KEY (DepartmentId)
      REFERENCES Departments (Id)
      ON DELETE CASCADE
      ON UPDATE CASCADE,

	  EmployeeTypeId INT NOT NULL,
	  CONSTRAINT FK_Employees_To_EmployeeTypes FOREIGN KEY (EmployeeTypeId)
      REFERENCES EmployeeTypes (Id)
      ON DELETE CASCADE
      ON UPDATE CASCADE,
   )
;
CREATE TABLE LoggedTimeTypes
(
Id INT IDENTITY (1,1)  NOT NULL, CONSTRAINT PK_LoggedTimeTypes PRIMARY KEY CLUSTERED (Id),
Description NVARCHAR(250) NOT NULL
)

CREATE TABLE LoggedTime
(
Id INT IDENTITY (1,1)  NOT NULL, CONSTRAINT PK_LoggedTime PRIMARY KEY CLUSTERED (Id),
DateLogged DATETIME NOT NULL,
Hours FLOAT NOT NULL,
LogType INT NOT NULL,

EmployeeId INT NOT NULL,
	  CONSTRAINT FK_LoggedTime_To_Employees FOREIGN KEY (EmployeeId)
      REFERENCES Employees (Id)
      ON DELETE CASCADE
      ON UPDATE CASCADE,

LoggedTimeTypeId INT NOT NULL,
	  CONSTRAINT FK_LoggedTime_To_LoggedTimeTypes FOREIGN KEY (LoggedTimeTypeId)
      REFERENCES LoggedTimeTypes (Id)
      ON DELETE CASCADE
      ON UPDATE CASCADE
)


